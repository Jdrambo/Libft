#include "libft.h"

void	ft_lstdelone(t_list **alst, void (*del)(void*, size_t))
{
	if (alst != NULL)
	{
		del((*alst)->content, (*alst)->content->size);
		free(*alst);
		*alst = NULL;
	}
}
